#include "ft_ls.h"

int		main(int ac, char **av)
{
	int i;

	i = ac;
	if (ac == 1 || ac == 2)
	{
		if (ac == 1)
			ft_ls(".");
		else
			ft_ls(av[i - 1]);
	}
	if (ac == 3 && (ft_strcmp(av[1],"-s") == 0))
		ft_stat(av[i - 1]);
	return (0);
}
