#include "ft_ls.h"

t_data			*lst_at(int i, t_data *lst)
{
	int			j;

	j = 0;
	while (lst)
	{
		if (i == j)
			return (lst);
		j++;
		lst = lst->next;
	}
	return (NULL);
}
