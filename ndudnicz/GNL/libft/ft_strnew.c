#include "libft.h"

char	*ft_strnew(size_t size)
{
	size_t		i;
	char		*new_str;

	i = 0;
	if ((new_str = (char*)malloc(size)) == NULL)
		return (NULL);
	ft_bzero(new_str, size);
	return (new_str);
}
