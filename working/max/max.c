int		max(int *tab, unsigned int len)
{
	int i;
	int max;

	i = 0;
	max = 0;
	if (!*tab || !len)
		return (0);
	while (len)
	{
		if (tab[i] > max)
			max = tab[i];
		i++;
		len--;
	}
	return (max);
}
