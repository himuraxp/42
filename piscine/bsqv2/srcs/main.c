#include "bsq.h"

int		main(int argc, char **argv)
{
	int		i;

	i = 1;
	if (argc == 1)
		ft_bsq("", 0);
	else
	{
		while (i < argc)
		{
			ft_bsq(argv[i], 1);
			i++;
		}
	}
	return (0);
}
