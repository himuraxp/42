void	ft_putstr(char *str);

int		main(int argc, char **argv)
{
	int i;

	i = 1;
	if (argc > 1)
	{
		while (argv[i] != '\0')
		{
			ft_putstr(argv[i]);
			i++;
		}
	}
	return (0);
}
