void	repeat_alpha(char *str)
{
	int i;
	int j;

	i = 0;
	j = 26;
	while ((str[i] >= 'A' && str[i] <= 'Z') || (str[i] >= 'a' && str[i] <= 'z'))
	{
		while (str[i] >= 'A' && str[i] <= 'Z')
			{
				if (str[i] > 'A')
				{
					j--;
					while ((str[i] + j) > 'A')
					write(1, str[i], 1);
				}
			}

		i++;
	}
}
