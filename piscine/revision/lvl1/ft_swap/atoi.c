int		ft_atoi(char *str)
{
	int i;
	int result;
	int sign;

	result = 0;
	sign = 1;
	i = 0;
	if (str[i] == '-')
	{
		sign = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9' && str[i] != '\0')
	{
		if (str[i] < '0' || str[i] > '9')
			break ;
		result = result * 10 + str[i] - '0';
		i++;
	}
	return (sign * result);
}
