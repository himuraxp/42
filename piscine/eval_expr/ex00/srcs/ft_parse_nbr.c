#include "ft_f.h"

int		ft_parse_nbr(char **ps)
{
	int		nbr;
	int		sign;

	nbr = 0;
	sign = 1;
	if ((*ps)[0] == '+' || (*ps)[0] == '-')
	{
		if ((*ps)[0] == '-')
			sign = -1;
		*ps = *ps + 1;
	}
	if ((*ps)[0] == '(')
	{
		*ps = *ps + 1;
		nbr = eval_sec(ps);
		if ((*ps)[0] == ')')
			*ps = *ps + 1;
		return (sign * nbr);
	}
	while ('0' <= (*ps)[0] && (*ps)[0] <= '9')
	{
		nbr = (nbr * 10) + (*ps)[0] - '0';
		*ps = *ps + 1;
	}
	return (sign * nbr);
}
